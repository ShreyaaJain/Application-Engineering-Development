    public void populateDoctorComboBox(){
        
        doctorJComboBox.removeAllItems();
        
        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if(organization instanceof DoctorOrganization)
            {
                for(UserAccount userAccount: organization.getUserAccountDirectory().getUserAccountList())
                {
                    doctorJComboBox.addItem(userAccount);
                }
            }
        }
    }
    
    public void populatePatientComboBox(){
        
        patientjComboBox.removeAllItems();
        
        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if(organization instanceof PatientOrganization)
            {
                //for(UserAccount userAccount: organization.getUserAccountDirectory().getUserAccountList())
                for(Patient patient : ((PatientOrganization)organization).getPatientDirectory().getPatientList())
                {
                    patientjComboBox.addItem(patient);
                }
            }
        }
        
    }

