    private void viewPatientJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPatientJButtonActionPerformed

        String message = messageJTextField.getText();
        
        ReceptionistWorkRequest request = new ReceptionistWorkRequest();
        request.setMessage(message);
        request.setSender(userAccount);
        request.setStatus("Sent");
        request.setReciever((UserAccount) doctorJComboBox.getSelectedItem());
        request.setPatient((Patient) patientjComboBox.getSelectedItem());
        //request.setPatient(() patientjComboBox.getSelectedItem());
        ((UserAccount) doctorJComboBox.getSelectedItem()).addPatient((Patient) patientjComboBox.getSelectedItem());

        Organization org = null;

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList())
        {
            if (organization instanceof DoctorOrganization)
            {
                org = organization;
                break;
            }
        }
        if (org!=null)
        {
            org.getWorkqueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }

    }//GEN-LAST:event_viewPatientJButtonActionPerformed

